namespace L5T1numbersFromAndTo
{
    class Program
    {
        static void Main()
        {
            for (int i = 0; i <= 100; i++)
            {
                Console.WriteLine(i);
            }

            Console.WriteLine();

            for (int j = 7; j <= 121; j++)
            {
                Console.WriteLine(j);
            }

            Console.WriteLine();

            for (int k = 100; k >= 30; k--)
            {
                Console.WriteLine(k);
            }
        }
    }
}